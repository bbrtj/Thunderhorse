package Thunderhorse::Router::Location;

use v5.40;
use Mooish::Base -standard;

use Future::AsyncAwait;
use Gears::X;

extends 'Gears::Router::Location::SigilMatch';

has option 'method' => (
	isa => Str,
);

has param 'name' => (
	isa => Str,
	lazy => 1,
);

has option 'to' => (
	isa => Str | CodeRef,
	predicate => 'implemented',
);

has param 'order' => (
	isa => Int,
	default => 0,
);

has param 'pagi' => (
	isa => Bool,
	default => false,
);

has param 'controller' => (
	isa => ConsumerOf ['Thunderhorse::Routable'],
);

has param 'pagi_app' => (
	isa => CodeRef,
	lazy => 1,
);

sub BUILD ($self, $)
{
	Gears::X->raise('controller has no action ' . $self->to)
		if $self->implemented && !$self->get_destination;

	# register the route in the router
	$self->router->_register_location($self->name, $self);
}

sub _build_name ($self)
{
	my $method = $self->method // 'ANY';
	my $pattern = $self->pattern;
	my $id = $self->router->_get_next_route_id;

	# autogenerated location name should be readable, but unique
	return "${id}_${method}_${pattern}";
}

sub _build_pagi_app ($self)
{
	weaken $self;
	my $dest = $self->get_destination;
	my $controller = $self->controller;

	if ($self->pagi) {
		# TODO: adjust PAGI (like Kelp did to PSGI)
		return async sub ($scope, @args) {
			my $result = await $dest->($scope, @args);
			$scope->{thunderhorse}->is_consumed(true);

			return $result;
		}
	}
	else {
		return async sub ($scope, $receive, $send) {
			Gears::X->raise('bad PAGI execution chain, not a thunderhorse app')
				unless exists $scope->{thunderhorse};

			my $ctx = $scope->{thunderhorse};
			$ctx->set_pagi([$scope, $receive, $send]);

			try {
				my $result = $dest->($controller, $ctx, $ctx->match->matched->@*);
				$result = await $result
					if $result isa 'Future';

				if (defined $result && !$ctx->is_consumed) {
					# TODO: result should be an array? (status, content_type, content)
					await $ctx->res->status(200)->html($result);
				}
			}
			catch ($ex) {
				die $ex unless $ex isa 'Gears::X::HTTP';
				# TODO: proper message
				await $ctx->res->status($ex->code)->text('Error')
				# TODO: log $ex->message
			}
		};
	}
}

sub get_destination ($self)
{
	my $to = $self->to;
	return $to if ref $to eq 'CODE';
	return $self->controller->can($self->to);
}

sub compare ($self, $path, $method)
{
	return undef if $self->has_method && $self->method ne $method;
	return $self->SUPER::compare($path);
}

